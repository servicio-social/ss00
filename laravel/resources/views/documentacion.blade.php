                <article id="art4">
                    <div id="oculto">
                        <h3>¿Cuáles son los requisitos?</h3>
                        <p>El alumno deberá contar con el 70% de los créditos aprobados de su plan de estudios.</p>

                        <p>Deberán cubrir 480 horas en un periodo no menor de 6 meses ni mayor de 2 años.</p>

                        <p>Haber asistido al Curso de Inducción</p>
                        <p>Al solicitar la prestación de Servicio Social el aspirante deberá haber seleccionado un programa o en su defecto proponerlo, debidamente avalado por la dependencia u organismo donde se pretende realizar.</p>

                        <p>El Servicio Social podrá presentarse únicamente en Dependencias Federales, Estatales o Municipales.</p>

                        <p>Cuando el alumno acuda a la oficina de Servicio Social a realizar su trámite deberá entregar:</p>

                        <p>Constancia del 70% de créditos</p>

                        <p>Fotografía infantil</p>

                        <p>Boletas de Actividades Extraescolares</p>
                    </div>
                    <button id="btnlm4" type="button" class="btn btn-default btn-lg btn-block">¿Cuáles son los requisitos?</button>
