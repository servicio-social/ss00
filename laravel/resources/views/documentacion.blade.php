                <article id="art2">
                    <div id="oculto">
                        <h3>¿Dónde puedes realizarlo?</h3>
                        <p>Los programas de Servicio Social internos y externos son las dos opciones donde puedes realizar tu Servicio Social.</p>

                        <p>Ellos te ofrecen la oportunidad de conocer la realidad profesional a través de la realización de la práctica de tu Servicio Social donde aplicarás tus conocimientos y desarrollarás tus habilidades.</p>

                        <p>Existen dos tipos de programas:</p>

                        <p>A. Programas Internos: Son para el fortalecimiento institucional y de desarrollo comunitario.</p>

                        <p>Diseñados y coordinados por académicos y personal de áreas de apoyo administrativo del ITC.</p>

                        <p>Incluyen aspectos de la docencia, formación y actualización docente, la investigación, el apoyo a diversos servicios educativos que brinda la Universidad y en menor grado, a la administración pública.</p>

                        <p>B. Programas Externos: Están diseñados y coordinados por personal de diversas instituciones públicas y del sector social.</p>

                        <p>Su desarrollo atiende y da solución a problemáticas que por su naturaleza involucran la acción de los profesionales de la educación.</p>                 
                    </div>
