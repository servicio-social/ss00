                <article id="art1">
                    <div id="oculto">
                        <h3>¿Cuándo relizarlo?</h3>
                        <p>Para que inicies el servicio social es necesario que hayas cubierto, como mínimo, el 70 % de los créditos de tu carrera, los que obtienes al concluir en forma regular el sexto semestre.</p>

                        <p>Hay dos periodos para iniciar el Servicio Social los cuales son cada inicio de semestre (enero y agosto). Para poder iniciarlo primeramente tienes que asistir a un curso de Inducción (es requisito indispensable para poder iniciar tu trámite), donde se te proporcionará información relacionada con tu Servicio Social.</p>

                        <p>Estos cursos se realizarán 2 veces al año: uno en mayo para iniciar tu servicio en agosto y otro en noviembre para iniciar tu servicio en enero.</p>

                        <p>Posteriormente a los cursos se asignará un día a cada alumno para acercarse a la Oficina de Servicio Social a entregar la documentación correspondiente.</p>
                    </div>
